import UIKit

extension UIColor {
    static var blueA800: UIColor {
        return UIColor(red: 0.16, green: 0.26, blue: 0.43, alpha: 1.00)
    }
    
    static var whiteBlue20: UIColor {
        return UIColor(red: 0.80, green: 0.89, blue: 1.00, alpha: 1.00)
    }
    
    static var blur: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.15)
    }
}
